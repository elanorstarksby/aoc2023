supports = {}
supported_by = {}


def is_overlapping(brick1, brick2):
    for i in range(3):
        if not (min(brick1[0][i], brick1[1][i]) <= max(brick2[0][i], brick2[1][i])
                and max(brick1[0][i], brick1[1][i]) >= min(brick2[0][i], brick2[1][i])):
            return False
    return True


def any_overlaps(brick, other_bricks):
    for lower_brick in other_bricks:
        if is_overlapping(brick, lower_brick):
            if lower_brick not in supports:
                supports[lower_brick] = []
            supports[lower_brick].append(brick)
            if brick not in supported_by:
                supported_by[brick] = []
            supported_by[brick].append(lower_brick)
    if brick in supported_by:
        return True
    return False


def can_fall_to(brick, other_bricks):
    return brick[0][2] > 0 and brick[1][2] > 0 and not any_overlaps(brick, other_bricks)


